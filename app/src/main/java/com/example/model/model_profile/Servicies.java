package com.example.model.model_profile;


public class Servicies {
    private String service_id;
    private String service_name;
    private String service_image;
    private double service_price;
    private int service_like;
    private int service_comment;
    private String service_liked;

    public String getService_id() {
        return service_id;
    }

    public void setService_id(String service_id) {
        this.service_id = service_id;
    }

    public String getService_name() {
        return service_name;
    }

    public void setService_name(String service_name) {
        this.service_name = service_name;
    }

    public String getService_image() {
        return service_image;
    }

    public void setService_image(String service_image) {
        this.service_image = service_image;
    }

    public double getService_price() {
        return service_price;
    }

    public void setService_price(double service_price) {
        this.service_price = service_price;
    }

    public int getService_like() {
        return service_like;
    }

    public void setService_like(int service_like) {
        this.service_like = service_like;
    }

    public int getService_comment() {
        return service_comment;
    }

    public void setService_comment(int service_comment) {
        this.service_comment = service_comment;
    }


    public String getService_liked() {
        return service_liked;
    }

    public void setService_liked(String service_liked) {

        this.service_liked = service_liked;

    }


}